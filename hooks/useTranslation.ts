// Helper to access nested properties using dot notation
const getNestedTranslation = (language: 'en' | 'it', key: string): string | undefined => {
  const langObject = translations[language];
  // Navigate through the nested object using the key parts
  return key.split('.').reduce((obj: any, k: string) => (obj ? obj[k] : undefined), langObject);
};

export const useTranslation = () => {
  const { language } = useContext(SettingsContext);

  /**
   * Translates a key into the current language.
   * @param key The key to translate (e.g., 'tabs.generate').
   * @param fallback An optional fallback string if the key is not found.
   * @returns The translated string, the fallback, or the key itself.
   */
  const t = (key: string, fallback?: string): string => {
    // A special case for material prompts, which might not be keys
    if (key.startsWith('material.') && !getNestedTranslation(language, key)) {
      return fallback || key; // Return the fallback or the raw value if it's not a valid key
    }

    const translation = getNestedTranslation(language, key);
    // If a translation is not found, use the provided fallback.
    // If no fallback is provided, return the key itself so it's clear what's missing.
    return translation ?? fallback ?? key;
  };

  return { t, language };
};